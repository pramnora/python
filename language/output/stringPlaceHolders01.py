# Python's use of placeholders...

# single string placeholder...
print("%s, Paul" % ("Hi"))
#Hi, Paul

# multiple string placeholders...
print("%s and %s ran up the hill" % ("Jack","Jill"))
#Jack and Jill ran up the hill

#both string and number placeholders...
print("My name is, %s; and, I'm currently aged: %d." % ("Paul",14))
#My name is, Paul; and, I'm currently aged: 14.

